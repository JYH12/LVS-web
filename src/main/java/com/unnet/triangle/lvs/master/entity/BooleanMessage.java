package com.unnet.triangle.lvs.master.entity;

public class BooleanMessage {

	boolean ok = false;
	String message = "";

	public boolean isOk() {
		return ok;
	}

	public void setOk(boolean ok) {
		this.ok = ok;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
